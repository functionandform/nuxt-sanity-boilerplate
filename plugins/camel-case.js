export default defineNuxtPlugin(() => {
  return {
    provide: {
      camelCase: (str) => {
        return str?.length ? str.replace(/(?:^\w|[A-Z]|\b\w)/g, function(word, index) {
          return index === 0 ? word.toLowerCase() : word.toUpperCase();
        }).replace(/\s+/g, '') : null
      }
    }
  }
})